package operator;

public class OperatorUnary {
    public static void main(String[] args) {
        int i = 0;
        i+=3;
        System.out.println(i);

        i+=3;
        System.out.println(i);

        i*=3;
        System.out.println(i);

        i*=3;
        System.out.println(i);
    }
}
